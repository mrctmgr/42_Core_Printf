#include "ft_printf.h"

int	ft_putuint(unsigned int num)
{
	int	i;

	i = 0;
	if (num > 9)
		i += ft_putuint(num / 10);
	write (1, &"0123456789"[num % 10], 1);
	i++;
	return (i);
}
